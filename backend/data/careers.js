function findMatch(query) {
  if (!query || typeof query !== "string") return null

  const normalized = query.toLowerCase().trim()

  for (const entry of careerData) {
    for (const keyword of entry.keywords) {
      if (normalized.includes(keyword) || keyword.includes(normalized)) {
        return entry
      }
    }
  }

  return null
}

function createCareer(data) {
  const id = data.name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "")

  const career = {
    name: data.name,
    id,
    keywords: data.keywords || [],
    programs: data.programs || [],
    careers: data.careers || [],
    skills: data.skills || [],
    institutions: data.institutions || [],
    overview: data.overview || "",
    whyChoose: data.whyChoose || "",
    benefits: data.benefits || [],
    responsibilities: data.responsibilities || [],
    technicalSkills: data.technicalSkills || [],
    softSkills: data.softSkills || [],
    educationalPathways: data.educationalPathways || "",
    careerOpportunities: data.careerOpportunities || [],
    futureOutlook: data.futureOutlook || "",
    advice: data.advice || "",
    followUpQuestions: data.followUpQuestions || [],
  }

  careerData.push(career)
  return career
}

function getAllCareers() {
  return careerData
}

function getCareerById(id) {
  return careerData.find((c) => c.id === id) || null
}

function updateCareer(id, data) {
  const idx = careerData.findIndex((c) => c.id === id)
  if (idx === -1) return null

  for (const key of Object.keys(data)) {
    if (data[key] !== undefined && key !== "id") {
      careerData[idx][key] = data[key]
    }
  }

  return careerData[idx]
}

function deleteCareer(id) {
  const idx = careerData.findIndex((c) => c.id === id)
  if (idx === -1) return false
  careerData.splice(idx, 1)
  return true
}

module.exports = { careerData, findMatch, createCareer, getAllCareers, getCareerById, updateCareer, deleteCareer }
